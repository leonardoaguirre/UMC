    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {

            ServicoContaService servico = new ServicoContaService();

            ServicoConta webservice = servico.getServicoConta();

            List<String> resultado = (List<String>) webservice.consultarDadosConta(Integer.parseInt(txtNumConta.getText()));

//Se resultado n√£o retornou vazio
            if (!resultado.get(0).equals("")) {

//Exibe resultado da busca
                lblNome.setText(resultado.get(0));

                lblSaldo.setText(resultado.get(1));

//Ativa a caixa de texto e bot√µes para Saque e Dep√≥sito
                txtValor.setEnabled(true);

                btnSaque.setEnabled(true);

                btnDeposito.setEnabled(true);

                txtValor.setText("");

            } else {
                lblNome.setText("");
                lblSaldo.setText("");

                txtValor.setEnabled(false);
                btnSaque.setEnabled(false);
                btnDeposito.setEnabled(false);
//Alerta se resultado n√£o encontrado
                JOptionPane.showMessageDialog(null, "Conta n√£o encontrada!");

            }

        } catch (Exception ex) {

            System.out.println("Erro: " + ex.getMessage());

        }
    }//GEN-LAST:event_btnConsultarActionPerformed

