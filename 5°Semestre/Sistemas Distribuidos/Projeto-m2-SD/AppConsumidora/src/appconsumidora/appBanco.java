    private void txtValorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorKeyReleased
        double saldo = Double.parseDouble(lblSaldo.getText());
        double valor = Double.parseDouble(txtValor.getText());
        if (valor <= saldo || valor < 0) {
            btnSaque.setEnabled(true);
        } else {
            btnSaque.setEnabled(false);
        }

    }//GEN-LAST:event_txtValorKeyReleased

    private void btnSaqueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaqueActionPerformed
        try {
            ServicoContaService servico = new ServicoContaService();
            ServicoConta webservice = servico.getServicoConta();
            List<String> resultado = (List<String>) webservice.consultarDadosConta(Integer.parseInt(txtNumConta.getText()));

            String aux = JOptionPane.showInputDialog(null, "Senha:", "Digite a senha da conta!", JOptionPane.INFORMATION_MESSAGE);
            if (aux == null) {
                JOptionPane.showMessageDialog(null, "Opera√ß√£o cancelada!");
            } else {
                if (aux.equals(resultado.get(2))) {
                    webservice.sacarConta(Integer.parseInt(txtNumConta.getText()), Double.parseDouble(txtValor.getText()));

                    btnConsultarActionPerformed(evt);
                    JOptionPane.showMessageDialog(null, "Saque realizado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Senha incorreta!", "Erro", JOptionPane.ERROR_MESSAGE);
                }

            }

        } catch (Exception ex) {

            System.out.println("Erro: " + ex.getMessage());

        }
    }//GEN-LAST:event_btnSaqueActionPerformed

