    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
        ServicoContaService servico = new ServicoContaService();
        ServicoConta webservice = servico.getServicoConta();

        if (!txtNome.getText().equals("") && !txtSenha.getText().equals("")) {
            
            if (webservice.criarConta(txtNome.getText(), txtSenha.getText())) {
                JOptionPane.showMessageDialog(null, "Conta criada com sucesso!");
                appBanco appBanco1 = new appBanco();
                this.setVisible(false);
                appBanco1.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao criar conta!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "NÃ£o deixe os campos vazios!");
        }
    }//GEN-LAST:event_btnCriarActionPerformed

